int	ft_sqrt(int nb)
{
	int	sqrt;

	sqrt = 0;
	while (sqrt * sqrt <= nb)
	{
		if (sqrt * sqrt == nb)
			return (sqrt);
		else if (sqrt > 46342)
			return (0);
		else
			sqrt++;
	}
	return (0);
}
